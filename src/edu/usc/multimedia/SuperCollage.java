package edu.usc.multimedia;

import java.util.List;

class SuperCollage {

	public List<Collage> collages;

	public SuperCollage() {
		collages = null;
	}

	public SuperCollage(List<Collage> collages) {
		this.collages = collages;
	}

	public List<Collage> getCollages() {
		return collages;
	}

	public void setCollages(List<Collage> collages) {
		this.collages = collages;
	}
}







